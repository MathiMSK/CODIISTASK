import mongoose from 'mongoose'

const user={
    firstname:{
        type:String,
        required:true
    },
    lastname:{
        type:String,
        required:true
    },
    username:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    }
}

const UserSchema=mongoose.model('User',user)

export default UserSchema